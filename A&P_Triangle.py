#This program caculates area and perimeter of a triangle.
side1=float(input("Please enter the height of the triangle: "))
side2=float(input('Please enter the base of the triangle: '))
side3=float(input('Please enter the hypotenuse of the triangle: '))
area=1/2*side1*side2
perimeter=side1+side2+side3
print('Area of the triangle is',area,'square unit.')
print('Perimeter of the triangle',perimeter,'unit.')